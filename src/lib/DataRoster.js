// Pre-compute and cache transformed schedules on initialization
const precomputeSchedules = () => {
	dataRoster.forEach(monthData => {
		const cacheKey = monthData.month;
		const transformedSchedules = monthData.crew_schedules.map(schedule => {
			const employee = employeeMap.get(schedule.employeeID);
			if (!employee) return null;
			
			// Convert duties array to days object once and cache it
			const days = {};
			const year = monthData.month.substring(0, 4);
			const monthNum = monthData.month.substring(5, 7);
			
			schedule.duties.forEach((duty, index) => {
				const dayNum = (index + 1).toString().padStart(2, '0');
				const dateKey = `${year}-${monthNum}-${dayNum}`;
				days[dateKey] = duty;
			});
			
			return {
				employeeID: schedule.employeeID,
				name: employee.name,
				rank: employee.rank,
				base: employee.base,
				days: days
			};
		}).filter(Boolean);
		
		scheduleCache.set(cacheKey, transformedSchedules);
	});
};

// Initialize cache on module load
precomputeSchedules();

// Optimized helper function to get schedule data for a specific month
export const getScheduleByMonth = (month) => {
	return dataRoster.find(roster => roster.month === month) || null;
};

// Cached version of getEmployeeSchedule
export const getEmployeeSchedule = (employeeId, month) => {
	const cacheKey = `${employeeId}-${month}`;
	
	if (employeeCache.has(cacheKey)) {
		return employeeCache.get(cacheKey);
	}
	
	const allSchedules = scheduleCache.get(month);
	if (!allSchedules) {
		employeeCache.set(cacheKey, null);
		return null;
	}
	
	const schedule = allSchedules.find(s => s.employeeID === employeeId);
	employeeCache.set(cacheKey, schedule || null);
	
	return schedule || null;
};

// Optimized function to get all schedules for a month - now just returns cached data
export const getAllSchedulesForMonth = (month) => {
	return scheduleCache.get(month) || [];
};

// Additional optimization: Filter schedules by base without re-processing
export const getSchedulesByBase = (month, base) => {
	const cacheKey = `${month}-${base}`;
	
	if (scheduleCache.has(cacheKey)) {
		return scheduleCache.get(cacheKey);
	}
	
	const allSchedules = scheduleCache.get(month) || [];
	const filteredSchedules = base === 'ALL' ? 
		allSchedules : 
		allSchedules.filter(schedule => schedule.base === base);
	
	// Cache filtered results for future use
	scheduleCache.set(cacheKey, filteredSchedules);
	
	return filteredSchedules;
};